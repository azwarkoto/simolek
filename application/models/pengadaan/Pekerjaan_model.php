    // get all
    function get_all()
    {
      $this->db->select('p.id, p.nama, p.kegiatan, s.nama_skpd, j.nama as jenis, m.nama as metode, p.pagu, p.realisasi');
      $this->db->from('simolek_p.pekerjaan p');
      $this->db->join('simolek.skpd s', 's.id_skpd=p.skpd', 'left');
      $this->db->join('simolek_p.jenis j', 'p.jenis=j.id', 'left');
      $this->db->join('simolek_p.metode m', 'p.metode=m.id', 'left');
      $this->db->order_by('p.id', 'desc');
      return $this->db->get()->result();
        // $this->db2->order_by($this->id, $this->order);
        // return $this->db2->get($this->table)->result();
    }

    // get data by id
    function get_by_id($id)
    {
      $this->db->select('p.id as id_p, p.nama, p.kegiatan, s.id_skpd, s.nama_skpd, j.id as id_j, j.nama as jenis, m.id as id_m, m.nama as metode, p.pagu, p.realisasi');
      $this->db->from('simolek_p.pekerjaan p');
      $this->db->join('simolek.skpd s', 'p.skpd=s.id_skpd', 'left');
      $this->db->join('simolek_p.jenis j', 'p.jenis=j.id', 'left');
      $this->db->join('simolek_p.metode m', 'p.metode=m.id', 'left');
      $this->db->where('p.id', $id);
      return $this->db->get('simolek_p.pekerjaan')->row();
    }

    // insert data
